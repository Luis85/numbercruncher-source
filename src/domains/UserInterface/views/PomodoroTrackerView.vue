The idea is to give the user a list of his created tasks from which he can decide on which to
include in the next pomodoro session. The user can gain experience and points based on difficulty
settings and stuff like this. The idea is to create something like a science tree out of the
categories of tasks the user creates. This would open up some nice plays like daily challenges and
stuff like this. The user can use the pomodoro tracker to gamify his day. The user can also checkin
his mood on a daily basis and create his own habit tracker. The tracker could also be used to create
timesheets on a daily basis and comit them to a project.
